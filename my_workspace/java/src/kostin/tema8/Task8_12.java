/*ѕрограмма должна создать массив из 12 случайных целых 
 * чисел из отрезка [-10;10] таким образом, чтобы отрицательных и 
 * положительных элементов там было поровну и не было нулей. 
 * ѕри этом пор€док следовани€ элементов должен быть случаен 
 * (т. е. не подходит вариант, когда в массиве посто€нно выпадает 
 * сначала 6 положительных, а потом 6 отрицательных чисел или же 
 * когда элементы посто€нно чередуютс€ через один и пр.). ¬ывести 
 * полученный массив на экран.
 */

package kostin.tema8;

public class Task8_12 {

	public static void main(String[] args) {
		int mas[] = new int[12];
		int count1 = 0, count2 = 0;
		while (true) {
			for (int i = 0; i < 12; i++) {
				mas[i] = (int) ((Math.random() * 22) - 11);
				if (mas[i] > 0)
					count1++;
				else if (mas[i] < 0)
					count2++;
				else {
					i = 0;
					count1 = 0;
					count2 = 0;
				}
			}
			if (count1 == count2) {
				break;
			} else {
				count1 = 0;
				count2 = 0;
			}

		}
		for (int i = 0; i < 12; i++) {
			System.out.print(mas[i] + " ");
		}

	}

}
