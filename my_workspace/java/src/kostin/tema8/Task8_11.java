/* Пользователь должен указать с клавиатуры чётное 
 * положительное число, а программа должна создать 
 * массив указанного размера из случайных целых чисел 
 * из [-5;5] и вывести его на экран в строку. После 
 * этого программа должна определить и сообщить 
 * пользователю о том, сумма модулей какой половины 
 * массива больше: левой или правой, либо сообщить, 
 * что эти суммы модулей равны. Если пользователь 
 * введёт неподходящее число, то программа должна 
 * требовать повторного ввода до тех пор, пока не 
 * будет указано корректное значение.
 */

package kostin.tema8;

import java.util.*;

public class Task8_11 {

	public static void main(String[] args) {
		
		int sum1 = 0, sum2 = 0, size = 0;
		Scanner sc = new Scanner(System.in);
		System.out.println("Введите целое четное число");
		while (true) {
			if (sc.hasNextInt()) {
				size = sc.nextInt();
				if (size % 2 == 0 && size > 0) {
					break;
				} else {
					System.out.println("ВВедите чётное положительное число");
				}
			}
		}

		int mas[] = new int[size];
		for (int i = 0; i < size; i++) {
			mas[i] = (int) ((Math.random() * 12) - 6);
			System.out.print(mas[i] + " ");
		}
		for (int i = 0, j = size / 2; i < size / 2 && j < size; i++, j++) {
			sum1 = sum1+(Math.abs(mas[i]));
			sum2 = sum2+(Math.abs(mas[j]));
		}
		if (sum1>sum2) System.out.println("\nЛевая половина больше "+sum1 );
		else if (sum2>sum1) System.out.println("\nПравая половина больше "+sum2);
		else System.out.println("\nРавны "+sum1);
	}
}
